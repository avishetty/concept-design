function BRONZE_BODY(qual) {
  return `-- bronze.${qual}
-- Raw landing · system of record · zero transformation · append-only.
-- Source columns land as VARCHAR / VARIANT; ingestion pipeline appends
-- the standard lineage columns and partitions by _ingestion_date.
create or replace table bronze.${qual} (
  -- ...source columns land as VARCHAR / VARIANT...
  raw_payload     variant,                 -- full source JSON preserved alongside flattened columns
  _ingested_at    timestamp,               -- when the record entered Bronze
  _source         varchar,                 -- source system identifier
  _batch_id       varchar,                 -- pipeline run id for lineage
  _ingestion_date date as (date(_ingested_at)) -- partition key
)
cluster by (_ingestion_date, _source);

-- Promotion gate: Bronze is append-only. Replay is driven from
-- (_ingestion_date, _source, _batch_id). Updates and deletes are
-- never applied here — CDC deletes surface as tombstones in S1.`;
}

