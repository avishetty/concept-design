function escapeHtml(s) {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

// ---- Open-in-editor menu -------------------------------------------------- //
// Each editor has its own URL handler. Clicking a row asks the OS to launch
// the editor at the given file. If the editor isn't installed locally the OS
// silently no-ops, which matches normal browser behaviour for unknown schemes.
const EDITORS = [
  { id: 'cursor',   label: 'Cursor',            scheme: 'cursor://file', icon: 'sparkle' },
  { id: 'vscode',   label: 'VS Code',           scheme: 'vscode://file', icon: 'code' },
  { id: 'vscodium', label: 'VSCodium',          scheme: 'vscodium://file', icon: 'code' },
  { id: 'idea',     label: 'JetBrains (IDEA)',  scheme: 'idea://open?file=', icon: 'bolt', encode: true },
  { id: 'fleet',    label: 'JetBrains Fleet',   scheme: 'fleet://file/',  icon: 'bolt' },
  { id: 'zed',      label: 'Zed',               scheme: 'zed://file',     icon: 'pulse' },
];

function OpenInEditorMenu({ filePath, workspaceRoot }) {
  const [open, setOpen] = React.useState(false);
  const ref = React.useRef(null);

  // Build an absolute(-looking) path the editor can resolve. We don't expand
  // `~` on the user's behalf — most editors accept it directly, and falling
  // back to the literal makes intent obvious if launching does fail.
  const root = (workspaceRoot || '~/walt').replace(/\/$/, '');
  const absPath = `${root}/${filePath}`;

  const buildHref = (ed) => {
    const target = ed.encode ? encodeURIComponent(absPath) : absPath;
    return `${ed.scheme}${target}`;
  };

  React.useEffect(() => {
    if (!open) return;
    const onClick = (e) => {
      if (ref.current && !ref.current.contains(e.target)) setOpen(false);
    };
    const onKey = (e) => { if (e.key === 'Escape') setOpen(false); };
    document.addEventListener('mousedown', onClick);
    document.addEventListener('keydown', onKey);
    return () => {
      document.removeEventListener('mousedown', onClick);
      document.removeEventListener('keydown', onKey);
    };
  }, [open]);

  return (
    <div ref={ref} style={{ position: 'relative', display: 'inline-flex' }}>
      <button
        onClick={() => setOpen(o => !o)}
        title="Open this file in a local editor"
        style={{
          display: 'inline-flex', alignItems: 'center', gap: 5,
          padding: '3px 8px',
          borderRadius: 6,
          background: open ? 'var(--bg-hover)' : 'transparent',
          border: '1px solid ' + (open ? 'var(--border-strong)' : 'var(--border-subtle)'),
          color: 'var(--text-secondary)',
          fontSize: 11, fontFamily: 'var(--font-sans)', fontWeight: 500,
          cursor: 'pointer',
          transition: 'background .12s, border-color .12s',
        }}
      >
        <Icon name="code" size={10} color="currentColor"/>
        Open in editor
        <Icon name={open ? 'chevD' : 'chevR'} size={9} color="var(--text-muted)"/>
      </button>
      {open && (
        <div style={{
          position: 'absolute', top: 'calc(100% + 6px)', right: 0, zIndex: 20,
          background: 'var(--bg-surface)',
          border: '1px solid var(--border-default, var(--border-subtle))',
          borderRadius: 10,
          boxShadow: '0 12px 28px rgba(17,20,24,0.10), 0 2px 6px rgba(17,20,24,0.05)',
          padding: 4,
          minWidth: 220,
        }}>
          <div style={{
            padding: '6px 10px 8px',
            fontSize: 10.5, color: 'var(--text-muted)',
            textTransform: 'uppercase', letterSpacing: 0.5, fontWeight: 600,
          }}>Open file in…</div>
          {EDITORS.map(ed => (
            <a
              key={ed.id}
              href={buildHref(ed)}
              onClick={() => setOpen(false)}
              style={{
                display: 'flex', alignItems: 'center', gap: 10,
                padding: '7px 10px',
                borderRadius: 6,
                fontSize: 12, color: 'var(--text-primary)',
                textDecoration: 'none', cursor: 'pointer',
              }}
              onMouseEnter={(ev) => ev.currentTarget.style.background = 'var(--bg-hover)'}
              onMouseLeave={(ev) => ev.currentTarget.style.background = 'transparent'}
            >
              <span style={{
                width: 22, height: 22, borderRadius: 6,
                background: 'var(--bg-inset)',
                color: 'var(--text-secondary)',
                display: 'inline-flex', alignItems: 'center', justifyContent: 'center',
                flexShrink: 0,
              }}>
                <Icon name={ed.icon} size={11} color="currentColor"/>
              </span>
              <span style={{ flex: 1, minWidth: 0 }}>{ed.label}</span>
              <Icon name="arrowR" size={9} color="var(--text-muted)"/>
            </a>
          ))}
          <div style={{
            borderTop: '1px solid var(--border-subtle)',
            margin: '6px 4px 2px',
            padding: '6px 8px 4px',
            fontSize: 10.5, color: 'var(--text-muted)',
            lineHeight: 1.5,
          }}>
            Launches via the editor's URL handler. Falls back silently if the app isn't installed.
          </div>
        </div>
      )}
    </div>
  );
}
