export function CodeArtifact() {
  const { ctx, shell } = usePhase();
  const branch = ctx?.branch || 'main';
  const committedTo = ctx?.gitConnected
    ? `${ctx.gitRemote || ctx.repoUrl || 'remote'} · ${branch}`
    : `local · ${branch}`;

  // If the user opened this artifact via a silver-code chip while a stage is
  // in review, surface that stage as the active file so they're reading the
  // exact code Walt is asking them to sign off on. Approval itself happens in
  // the chat — the side panel is information-only.
  const reviewStage = ctx?.silverStage && ['s1','s2','s3'].includes(ctx.silverStage)
    && shell?.silverStageStatus?.[ctx.silverStage] === 'review'
    && !ctx?.silverApproved?.[ctx.silverStage]
    ? ctx.silverStage
    : null;

  const initialActive = reviewStage ? STAGE_FILE[reviewStage] : 'silver/s1_dedup/finance_ap_invoices.sql';
  const initialExpanded = reviewStage ? STAGE_EXPAND[reviewStage] : { silver: true, s1_dedup: true };
  const [active, setActive] = React.useState(initialActive);
  const [expanded, setExpanded] = React.useState(initialExpanded);

  // When the review stage changes (e.g. S1 just flipped to 'review'), pivot the
  // tree to that stage's file so the bar always points at the right code.
  React.useEffect(() => {
    if (!reviewStage) return;
    setActive(STAGE_FILE[reviewStage]);
    setExpanded(e => ({ ...e, ...STAGE_EXPAND[reviewStage] }));
  }, [reviewStage]);

  const findFile = (nodes, path) => {
    for (const n of nodes) {
      if (n.type === 'file' && joinPath(n.__parent, n.name) === path) return n;
      if (n.type === 'folder') {
        const f = findFile(n.children.map(c => ({ ...c, __parent: joinPath(n.__parent, n.name) })), path);
        if (f) return f;
      }
    }
    return null;
  };

  const annotated = annotate(TREE, '');
  const current = findFile(annotated, active);
  const body = current?.body || '-- select a file';
  const isYaml = active.endsWith('.yaml');

  const toggle = (key) => setExpanded(e => ({ ...e, [key]: !e[key] }));

  return (
    <div style={{ display: 'flex', height: '100%', minHeight: 0 }}>
      <div style={{
        width: 260, flexShrink: 0,
        borderRight: '1px solid var(--border-subtle)',
        background: 'var(--bg-surface)',
        overflowY: 'auto',
        padding: '10px 8px 14px',
      }}>
        <div style={{
          padding: '4px 10px 8px',
          fontSize: 10.5, color: 'var(--text-muted)',
          textTransform: 'uppercase', letterSpacing: 0.5, fontWeight: 600,
        }}>
          finance-platform
        </div>
        <Tree nodes={annotated} expanded={expanded} active={active} onToggle={toggle} onPick={setActive}/>
      </div>

      <div style={{ flex: 1, minWidth: 0, display: 'flex', flexDirection: 'column', background: 'var(--code-bg)' }}>
        <div style={{
          padding: '8px 14px',
          borderBottom: '1px solid var(--border-subtle)',
          background: 'var(--bg-inset)',
          display: 'flex', alignItems: 'center', gap: 8,
          fontFamily: 'var(--font-mono)', fontSize: 11.5, color: 'var(--text-primary)',
        }}>
          <Icon name="file" size={11} color="var(--text-muted)"/>
          {active}
          <div style={{ flex: 1 }}/>
          <span style={{ fontSize: 10.5, color: 'var(--text-muted)' }}>read-only · committed to <span className="walt-mono">{committedTo}</span></span>
          <OpenInEditorMenu filePath={active} workspaceRoot={ctx?.localPath || '~/walt'}/>
        </div>

        {reviewStage && <ReviewStatusBanner stage={reviewStage}/>}

        <div style={{
          flex: 1, minHeight: 0, overflow: 'auto',
          padding: '14px 16px',
          fontFamily: 'var(--font-mono)',
          fontSize: 12, lineHeight: 1.65,
          color: 'var(--text-primary)',
          whiteSpace: 'pre',
        }}
        dangerouslySetInnerHTML={{ __html: isYaml ? escapeHtml(body) : hilite(body) }}
        />
      </div>
    </div>
  );
}

function joinPath(a, b) {
  if (!a) return b;
  return a + '/' + b;
}

function annotate(nodes, parent) {
  return nodes.map(n => ({
    ...n,
    __parent: parent,
    ...(n.children ? { children: annotate(n.children, joinPath(parent, n.name)) } : {}),
  }));
}

function Tree({ nodes, expanded, active, onToggle, onPick, depth = 0 }) {
  return (
    <div style={{ display: 'flex', flexDirection: 'column' }}>
      {nodes.map(n => {
        if (n.type === 'folder') {
          const open = expanded[n.name];
          return (
            <div key={joinPath(n.__parent, n.name)}>
              <button
                onClick={() => onToggle(n.name)}
                style={{
                  display: 'inline-flex', alignItems: 'center', gap: 6,
                  width: '100%', textAlign: 'left',
                  padding: '4px 8px', paddingLeft: 8 + depth * 12,
                  background: 'transparent', border: 'none',
                  fontSize: 12, color: 'var(--text-primary)',
                  fontFamily: 'var(--font-mono)', cursor: 'pointer',
                  borderRadius: 4,
                }}
              >
                <Icon name={open ? 'chevD' : 'chevR'} size={10} color="var(--text-muted)"/>
                <Icon name="folder" size={11} color="var(--accent)"/>
                {n.name}
              </button>
              {open && <Tree nodes={n.children} expanded={expanded} active={active} onToggle={onToggle} onPick={onPick} depth={depth + 1}/>}
            </div>
          );
        }
        const path = joinPath(n.__parent, n.name);
        const on = active === path;
        return (
          <button
            key={path}
            onClick={() => onPick(path)}
            style={{
              display: 'inline-flex', alignItems: 'center', gap: 6,
              width: '100%', textAlign: 'left',
              padding: '4px 8px', paddingLeft: 8 + depth * 12,
              background: on ? 'var(--accent-soft)' : 'transparent',
              border: 'none',
              fontSize: 12, color: on ? 'var(--accent)' : 'var(--text-secondary)',
              fontFamily: 'var(--font-mono)', cursor: 'pointer',
              borderRadius: 4,
              fontWeight: on ? 600 : 500,
            }}
          >
            <span style={{ width: 10 }}/>
            <Icon name="file" size={10} color={on ? 'var(--accent)' : 'var(--text-muted)'}/>
            {n.name}
          </button>
        );
      })}
    </div>
  );
}

